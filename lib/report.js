// Render text into a terminal-style PNG using the bitmap font
// ---------------------------------------------------------------------------

function renderTextImage(text, outputPath) {
  const scale = 3;
  const charW = 5, charH = 7;
  const cellW = (charW + 1) * scale;
  const cellH = (charH + 2) * scale;
  const padX = 24, padY = 20;
  const headerH = 36;
  const maxCols = 50;

  const lines = [];
  for (let i = 0; i < text.length; i += maxCols) {
    lines.push(text.substring(i, i + maxCols));
  }

  const W = maxCols * cellW + padX * 2;
  const H = lines.length * cellH + padY * 2 + headerH;
  const pixels = Buffer.alloc(W * H * 3);

  const setPixel = (x, y, r, g, b) => {
    if (x < 0 || x >= W || y < 0 || y >= H) return;
    const off = (y * W + x) * 3;
    pixels[off] = r; pixels[off+1] = g; pixels[off+2] = b;
  };

  const fillRect = (x0, y0, w, h, r, g, b) => {
    for (let dy = 0; dy < h; dy++)
      for (let dx = 0; dx < w; dx++)
        setPixel(x0 + dx, y0 + dy, r, g, b);
  };

  // Background
  fillRect(0, 0, W, H, 24, 24, 32);

  // Header bar
  fillRect(0, 0, W, headerH, 38, 38, 50);

  // Window dots
  fillRect(14, 14, 10, 10, 255, 95, 86);
  fillRect(30, 14, 10, 10, 255, 189, 46);
  fillRect(46, 14, 10, 10, 39, 201, 63);

  // Header title (draw "ENCRYPTED OUTPUT" using font)
  const title = 'ENCRYPTED OUTPUT';
  const titleStartX = 70;
  for (let ci = 0; ci < title.length; ci++) {
    const glyph = GLYPH[title[ci]];
    if (!glyph) continue;
    for (let row = 0; row < charH; row++) {
      const bits = glyph[row];
      for (let col = 0; col < charW; col++) {
        if (bits & (1 << (charW - 1 - col))) {
          const px = titleStartX + ci * (charW + 1) * 2 + col * 2;
          const py = 10 + row * 2;
          fillRect(px, py, 2, 2, 180, 185, 200);
        }
      }
    }
  }

  // Render each line of encrypted text
  for (let li = 0; li < lines.length; li++) {
    const line = lines[li];
    const baseY = headerH + padY + li * cellH;
    for (let ci = 0; ci < line.length; ci++) {
      const ch = line[ci];
      const glyph = GLYPH[ch];
      if (!glyph) continue;
      const baseX = padX + ci * cellW;
      for (let row = 0; row < charH; row++) {
        const bits = glyph[row];
        for (let col = 0; col < charW; col++) {
          if (bits & (1 << (charW - 1 - col))) {
            const px = baseX + col * scale;
            const py = baseY + row * scale;
            fillRect(px, py, scale, scale, 0, 230, 118);
          }
        }
      }
    }
  }

  // Build PNG
  const rows = [];
  for (let y = 0; y < H; y++) {
    const row = Buffer.alloc(1 + W * 3);
    row[0] = 0;
    pixels.copy(row, 1, y * W * 3, (y + 1) * W * 3);
    rows.push(row);
  }
  const raw = Buffer.concat(rows);

  function chunk(type, data) {
    const combined = Buffer.concat([Buffer.from(type), data]);
    const len = Buffer.alloc(4);
    len.writeUInt32BE(data.length);
    const crc = Buffer.alloc(4);
    crc.writeUInt32BE(zlib.crc32(combined) >>> 0);
    return Buffer.concat([len, combined, crc]);
  }

  const ihdr = Buffer.alloc(13);
  ihdr.writeUInt32BE(W, 0);
  ihdr.writeUInt32BE(H, 4);
  ihdr[8] = 8; ihdr[9] = 2;

  const png = Buffer.concat([
    Buffer.from('89504e470d0a1a0a', 'hex'),
    chunk('IHDR', ihdr),
    chunk('IDAT', zlib.deflateSync(raw, { level: 6 })),
    chunk('IEND', Buffer.alloc(0)),
  ]);

  fs.writeFileSync(outputPath, png);
}

